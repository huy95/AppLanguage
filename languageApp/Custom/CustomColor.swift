import Foundation
import UIKit
extension UIColor {
    class func colorNavigation() -> UIColor{
        return UIColor(red: 0, green: 95/255, blue: 208/255, alpha: 1)
    }
    class func colorButtonMain() -> UIColor {
        return UIColor.init(red: 0, green: 95/255, blue: 208/255, alpha: 1)
    }
    class func colorBackground() -> UIColor {
        return UIColor.init(red: 216/255, green: 216/255, blue: 216/255, alpha: 100)
    }
}
